##
# #introduction takes in a name and outputs an introduction containing that name (using string interpolation)
##
def introduction(name)
  puts "Hi, my name is #{name}.";
end

##
# #introduction_with_language takes in a name and a language and outputs and introduction with the name and language (using string interpolation)
##
def introduction_with_language(name, language)
  puts "Hi, my name is #{name} and I am learning to program in #{language}."
end
